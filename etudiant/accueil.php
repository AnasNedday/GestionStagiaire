<p></br></br></br></br>Le stage est un atout incontournable dans un cursus de formation. C’est une étape d’initiation très riche en enseignements dont l’objectif est de permettre aux stagiaires </p><p> d’acquérir une expérience et une connaissance pratique de la vie de l’entreprise qui les reçoit : son organisation, ses femmes et hommes, ses activités au quotidien…</p><p> Dans un souci de veille au sens le plus large, la RADEEJ offre, aux jeunes étudiants , l’opportunité de mettre en pratique les connaissances acquises </p><p> pendant leurs études et plus particulièrement dans leurs domaines spécifiques de compétence.</p>

L’Office bénéficie, de son côté, de l’apport des jeunes stagiaires qui font preuve d’innovation. Dans cette perspective, lA RADEEJ accorde 3 types de stage :</p>
<p><h2> Stage de fin d'études (PFE)</h2></p>
<p>Le stagiaire prépare un projet qui s’inscrit dans le cadre d’un projet d’entité et qui serait susceptible d’assister les unités de la RADEEJ à trouver une solution pratique à une problématique spécifique.</p>
	<p><h2>Stage technique</h2></p>
<p>Se déroule en été, pour les étudiants de 2ème année. Le stagiaire met en application ses connaissances théoriques dispensées dans son établissement et ce dans le cadre d'un projet de l’Office.</p>
<p><h2> Stage ouvrier</h2></p>
<p>Se déroule entre Juillet et Septembre, pour les étudiants de la 1ère année. Compte tenu de sa durée, ce stage vise à offrir à l’étudiant l’opportunité d’entamer ses premiers contacts avec le milieu du travail.</p>
